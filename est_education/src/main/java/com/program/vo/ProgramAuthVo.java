package com.program.vo;

import common.base.vo.BaseVo;

public class ProgramAuthVo extends BaseVo {
	
	String programCd; // 프로그램 코드
	String programAuthCd; //프로그램 권한코드
	String programAuthNm; //권한 명
	String isAuthSelect; //조회권한여부
	String isAuthSave; //저장권한여부
	String isAuthExcel; //엑셀권한여부
	
	public ProgramAuthVo() { super(); }
	
	public String getProgramCd() {
		return programCd;
	}
	public void setProgramCd(String programCd) {
		this.programCd = programCd;
	}
	public String getProgramAuthCd() {
		return programAuthCd;
	}
	public void setProgramAuthCd(String programAuthCd) {
		this.programAuthCd = programAuthCd;
	}
	public String getProgramAuthNm() {
		return programAuthNm;
	}
	public void setProgramAuthNm(String programAuthNm) {
		this.programAuthNm = programAuthNm;
	}
	public String getIsAuthSelect() {
		return isAuthSelect;
	}
	public void setIsAuthSelect(String isAuthSelect) {
		this.isAuthSelect = isAuthSelect;
	}
	public String getIsAuthSave() {
		return isAuthSave;
	}
	public void setIsAuthSave(String isAuthSave) {
		this.isAuthSave = isAuthSave;
	}
	public String getIsAuthExcel() {
		return isAuthExcel;
	}
	public void setIsAuthExcel(String isAuthExcel) {
		this.isAuthExcel = isAuthExcel;
	}

	public ProgramAuthVo(String programCd, String programAuthCd, String programAuthNm, String isAuthSelect,
			String isAuthSave, String isAuthExcel) {
		super();
		this.programCd = programCd;
		this.programAuthCd = programAuthCd;
		this.programAuthNm = programAuthNm;
		this.isAuthSelect = isAuthSelect;
		this.isAuthSave = isAuthSave;
		this.isAuthExcel = isAuthExcel;
	}

	@Override
	public String toString() {
		return "ProgramAuthVo [programCd=" + programCd + ", programAuthCd=" + programAuthCd + ", programAuthNm="
				+ programAuthNm + ", isAuthSelect=" + isAuthSelect + ", isAuthSave=" + isAuthSave + ", isAuthExcel="
				+ isAuthExcel + "]";
	}
	
}
