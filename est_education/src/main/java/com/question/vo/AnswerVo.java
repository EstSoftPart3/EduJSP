package com.question.vo;

import java.sql.Timestamp;

import common.base.vo.BaseVo;

public class AnswerVo extends BaseVo {
	
	int basNo;
	String basTtl;
	String basCtt;
	String basRgl;
	Timestamp basRgtd;
	Timestamp basMdfd;
	
	
	//답변
	public int getBasNo() {
		return basNo;
	}
	public void setBasNo(int basNo) {
		this.basNo = basNo;
	}
	public String getBasTtl() {
		return basTtl;
	}
	public void setBasTtl(String basTtl) {
		this.basTtl = basTtl;
	}
	public String getBasCtt() {
		return basCtt;
	}
	public void setBasCtt(String basCtt) {
		this.basCtt = basCtt;
	}
	public String getBasRgl() {
		return basRgl;
	}
	public void setBasRgl(String basRgl) {
		this.basRgl = basRgl;
	}
	public Timestamp getBasRgtd() {
		return basRgtd;
	}
	public void setBasRgtd(Timestamp basRgtd) {
		this.basRgtd = basRgtd;
	}
	public Timestamp getBasMdfd() {
		return basMdfd;
	}
	public void setBasMdfd(Timestamp basMdfd) {
		this.basMdfd = basMdfd;
	}

}
